#calculator- practicing defining functions

#addition function
def add(a,b):
  return a+b
  
#subtraction function
def subtract(a,b):
  return a-b

#multiply function
def multiply(a,b):
  return a*b

#divide function
def divide(a,b):
  return a/b

#exponent function
def exp(a,b):
  return a**b

#calling the function and printing the return value

print(add(5,5))
print(subtract(5,5))
print(multiply(5,5))
print(divide(5,5))
print(exp(5,2))